using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace TournamentTracker
{
    public class PersonModel
    {
        /// <summary>
        /// Represents the First Name of a team player.
        /// </summary>
        public string FirstName { get; set; }

        /// <summary>
        /// Represents the Last Name of a team player.
        /// </summary>
        public string LastName { get; set; }

        /// <summary>
        /// Represents the Email address of a team player.
        /// </summary>
        public string EmailAddress { get; set; }

        /// <summary>
        /// Represents the Cell phone number of a team player.
        /// </summary>
        public string CellPhoneNumber { get; set; }
    }
}
